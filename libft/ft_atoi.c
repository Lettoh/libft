#include "libft.h"

int		ft_atoi(const char *str)
{
	long				i;
	int					sign;
	unsigned long long	n;

	i = 0;
	n = 0;
	sign = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = 10 * n + str[i] - '0';
		i++;
	}
	if (sign == 1)
		return (-n);
	else
		return (n);
}
