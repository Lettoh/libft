#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*str;
	int		len;

	len = 0;
	i = 0;
	while (src[len])
		len++;
	str = (char*)malloc(sizeof(*src) * (len + 1));
	if (str == NULL)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
